package BOX;

public class Box {
     private int length;
     private int breath;
     private int height;
     
     public Box(int length, int breath, int height) {
		super();
		this.length = length;
		this.breath = breath;
		this.height = height;
	}

	 public int getLength() {
		 return length;
	 }
	 
	 public int getBreath() {
		 return breath;
	 }
	
	 public int getHeight() {
		 return height;
	 }
	 
	 
     
     
     
}
